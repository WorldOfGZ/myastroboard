def get_catalogue_reports(catalogue_dir: str) -> dict:
    """
    Get all available reports for a target
    Returns dictionary with report types and their data
    """

    reports = {}

    base_path = _resolve_catalogue_dir(catalogue_dir)
    if not base_path:
        return reports

    report_files = {
        'objects': 'uptonight-report.json',
        'comets': 'uptonight-comets-report.json',
        'bodies': 'uptonight-bodies-report.json',
        'plot': 'uptonight-plot.png'
    }

    for report_type, filename in report_files.items():
        if filename.endswith('.json'):
            parsed = parse_uptonight_report(filename, report_type, str(base_path))
            if parsed:
                reports[report_type] = parsed
        else:
            file_path = _resolve_path_within(base_path, filename)
            if not file_path or not file_path.exists():
                continue
            reports[report_type] = {
                'available': True,
                'path': filename
            }

    return reports


def format_value(key: str, value) -> str:
    """Format values for display"""
    if value is None or value == '':
        return 'N/A'
    
    # Handle numeric formatting
    if isinstance(value, (int, float)):
        # Foto is a percentage
        if key == 'foto':
            return f"{value * 100:.1f}%"
        # Magnitudes
        elif key in ['mag', 'visual magnitude', 'absolute magnitude']:
            return f"{value:.2f}"
        # Coordinates
        elif key in ['right ascension', 'declination', 'altitude', 'azimuth']:
            return f"{value:.2f}°"
        # Distances
        elif 'distance' in key.lower():
            return f"{value:.3f} AU"
        # Sizes
        elif key == 'size':
            return f"{value:.1f}'"
        else:
            return str(value)
    
    return str(value)


def sanitize_alttime_target_name(name: str) -> str:
    """
    Sanitize a target name to be a safe filename.
    Preserves letters, numbers, dots, dashes, underscores, and parentheses.
    """
    name = name.lower()
    # autoriser a-z, 0-9, ., _, -, (, )
    name = re.sub(r'[^a-z0-9._\-\(\)]', '-', name)
    name = re.sub(r'\.{2,}', '.', name)    # collapse multiple dots
    name = name.strip('-.')                 # trim leading/trailing dash/dot
    return name or "target"


def get_alttime_file_name(target_name: str, catalogue_dir: str) -> str:
    """
    Generate Alttime-compatible file name from target name.
    Return file name if exists, else empty string.
    """
    sanitized = sanitize_alttime_target_name(target_name)
    base_path = _resolve_catalogue_dir(catalogue_dir)
    if not base_path:
        return ""

    filename = f"uptonight-alttime-{sanitized}.png"
    file_path = _resolve_path_within(base_path, filename)
    if not file_path:
        return ""

    if file_path.exists() and file_path.is_file():
        return filename
    else:
        return ""