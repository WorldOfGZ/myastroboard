"""
UpTonight output parser
Parses JSON reports generated by UpTonight
"""
import os
import json
from pathlib import Path
import re
from typing import Dict, List, Optional
from logging_config import get_logger

# Initialize logger for this module
logger = get_logger(__name__)


def _resolve_catalogue_dir(catalogue_dir: str) -> Optional[Path]:
    """Resolve and validate a catalogue directory path."""
    try:
        base_path = Path(catalogue_dir).resolve()
    except (TypeError, ValueError, RuntimeError):
        return None

    if not base_path.exists() or not base_path.is_dir():
        return None

    return base_path


def _resolve_path_within(base_path: Path, candidate_path: str) -> Optional[Path]:
    """Resolve a candidate path and ensure it stays within base_path."""
    try:
        candidate = Path(candidate_path)
        resolved = (base_path / candidate).resolve() if not candidate.is_absolute() else candidate.resolve()
        resolved.relative_to(base_path)
        return resolved
    except (TypeError, ValueError, RuntimeError):
        return None

def parse_uptonight_report(file_path: str, report_type: str, catalogue_dir: str) -> Optional[dict]:
    """
    Parse UpTonight JSON report file safely.
    Returns parsed data or None if file doesn't exist or is invalid.
    """
    try:
        base_path = _resolve_catalogue_dir(catalogue_dir)
        if not base_path:
            return None

        expected_files = {
            'objects': 'uptonight-report.json',
            'comets': 'uptonight-comets-report.json',
            'bodies': 'uptonight-bodies-report.json'
        }

        if report_type in expected_files:
            safe_file_path = _resolve_path_within(base_path, expected_files[report_type])
        else:
            safe_file_path = _resolve_path_within(base_path, Path(file_path).name)

        if not safe_file_path:
            # Path traversal attempt, ignore
            logger.warning(f"Attempted access outside catalogue_dir: {file_path}")
            return None

        if not safe_file_path.exists() or not safe_file_path.is_file():
            return None

        with safe_file_path.open('r', encoding='utf-8') as f:
            data = json.load(f)

        if report_type == 'objects':
            return parse_objects_report(data, str(base_path))
        elif report_type == 'comets':
            return parse_comets_report(data, str(base_path))
        elif report_type == 'bodies':
            return parse_bodies_report(data, str(base_path))
        else:
            return {'type': 'unknown', 'raw': data}

    except Exception:
        logger.exception(f"Error parsing UpTonight report {file_path}")
        return None


def parse_objects_report(data: dict, catalogue_dir: str) -> dict:
    """Parse objects (targets) report into list of objects with safe Alttime filenames"""
    objects = []

    if 'id' not in data:
        return {'type': 'objects', 'objects': []}

    num_entries = len(data['id'])

    base_path = _resolve_catalogue_dir(catalogue_dir)
    safe_catalogue_dir = str(base_path) if base_path else ""

    for i in range(num_entries):
        obj = {}
        for key, values in data.items():
            if str(i) in values:
                if key == 'id':
                    # Use safe file path generator
                    obj['alttime_file'] = get_alttime_file_name(values[str(i)], safe_catalogue_dir)
                obj[key] = values[str(i)]
        objects.append(obj)

    return {
        'type': 'objects',
        'count': len(objects),
        'objects': objects
    }


